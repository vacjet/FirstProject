package Study;

public class PrimitiveDataTypes2 {
    public static void main(String[] args) {

        byte b1 = 12;
        byte b2 = 0xC;
        byte b3 = 014;
        byte b4 = 0b1100;

        short s1 = -1300;
        short s2 = -0x514;
        short s3 = -02424;
        short s4 = -0b010100010100;

        int i1 = 500;
        int i2 = -1000;
        int i3 = 0;

        long l1 = 1000;
        long l2 = 10_000_000_000L;
        long l3 = 100L;

        float f1 = 2.5F;
        float f2 = 3.698f;

        double d1 = 8.444D;
        double d2 = 10.658d;
        double d3 = 102.45;

        char c1 = '5';
        char c2 = 'a';
        char c3 = 215;
        char c4 = '\u1234';

        boolean bool1 = true;
        boolean bool2 = false;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);

        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
        System.out.println(s4);

    }
}
